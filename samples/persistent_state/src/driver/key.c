#include "key.h"

uint key_read() {
	uint t = 0;
	
	P44 = 0; P42 = 1; P35 = 1; P34 = 1;
	if (P33 == 0) t |= 1 << 0;
	if (P32 == 0) t |= 1 << 1;
	if (P31 == 0) t |= 1 << 2;
	if (P30 == 0) t |= 1 << 3;

	P44 = 1; P42 = 0; P35 = 1; P34 = 1;
	if (P33 == 0) t |= 1 << 4;
	if (P32 == 0) t |= 1 << 5;
	if (P31 == 0) t |= 1 << 6;
	if (P30 == 0) t |= 1 << 7;
	
	P44 = 1; P42 = 1; P35 = 0; P34 = 1;
	if (P33 == 0) t |= 1 << 8;
	if (P32 == 0) t |= 1 << 9;
	if (P31 == 0) t |= 1 << 10;
	if (P30 == 0) t |= 1 << 11;
	
	P44 = 1; P42 = 1; P35 = 1; P34 = 0;
	if (P33 == 0) t |= 1 << 12;
	if (P32 == 0) t |= 1 << 13;
	if (P31 == 0) t |= 1 << 14;
	if (P30 == 0) t |= 1 << 15;
	
	P44 = 1; P42 = 1; P35 = 1; P34 = 1;
	return t;
}